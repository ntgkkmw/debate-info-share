'use client';

import { useRouter } from 'next/navigation';
import styles from './page.module.css';

const USAGE_GUIDE = `
カテゴリー管理アプリの使い方

1. カテゴリーの作成
   - ホームページの「新規カテゴリー作成」セクションで、新しいカテゴリー名を入力します。
   - 「カテゴリーを作成」ボタンをクリックして、新しいカテゴリーを追加します。

2. カテゴリーの表示
   - 作成したカテゴリーは「作成済みカテゴリー一覧」に表示されます。
   - カテゴリー名をクリックすると、そのカテゴリーの詳細ページに移動します。

3. カテゴリーのロック/アンロック
   - 各カテゴリーの左側にあるチェックボックスをクリックすると、カテゴリーをロックまたはアンロックできます。
   - ロックされたカテゴリーは削除できません。

4. カテゴリーの削除
   - カテゴリーの右側にある「削除」ボタンをクリックすると、カテゴリーを削除できます。
   - ロックされているカテゴリーは削除できません。
   - 削除の前に確認画面が表示されます。

5. カテゴリー内のコンテンツ管理
   - カテゴリー名をクリックしてカテゴリーページに移動すると、そのカテゴリー内のコンテンツを管理できます。
   - 新しいコンテンツを追加するには、タイトル、URL、説明（オプション）を入力し、「追加」ボタンをクリックします。
   - 追加されたコンテンツは一覧で表示されます。

6. コンテンツの操作
   - コンテンツのタイトルをクリックすると、関連するURLが新しいタブで開きます。
   - 各コンテンツには「閲覧」「編集」「削除」ボタンがあります。
     - 「閲覧」: コンテンツの詳細を表示します。
     - 「編集」: コンテンツの情報を編集できます。
     - 「削除」: コンテンツを削除します（確認画面あり）。

7. アプリの使い方
   - ホームページの「使い方」ボタンをクリックすると、このガイドを確認できます。

このアプリを使用することで、ウェブサイトやリソースを効率的に整理し、簡単にアクセスできるようになります。
カテゴリーを作成してコンテンツを整理し、必要な情報をすぐに見つけることができます。

※これだけではわからないと思うので、動画説明をご覧ください。
`;

export default function Usage() {
  const router = useRouter();

  return (
    <div className={styles.container}>
      <h1>使い方</h1>
      <pre className={styles.usageGuide}>{USAGE_GUIDE}</pre>
      <button onClick={() => router.back()} className={styles.backButton}>
        戻る
      </button>
    </div>
  );
}